#!/usr/bin/env node

import fs from "node:fs";
import path from "node:path";

const draftLanguages = ["de", "it", "sv", "hr", "nl", "no", "fr", "es", "ro"];
const outputJsonPath = path.join("docs", "i18n", "generated", "draft-runtime-dictionaries.json");
const outputSourcePath = path.join("src", "generated", "draft-i18n-dictionaries.js");

const packPaths = process.argv.slice(2);

function usage() {
  return [
    "Usage: node scripts/generate-draft-i18n-runtime-dictionaries.mjs <translation-pack.json> [translation-pack.json ...]",
    "",
    "If no paths are provided, the script reads the standard draft packs for de, it, sv, hr, nl, no, fr, es, and ro.",
  ].join("\n");
}

function readJson(filePath) {
  try {
    return JSON.parse(fs.readFileSync(filePath, "utf8"));
  } catch (error) {
    throw new Error(`${filePath}: could not read or parse JSON: ${error.message}`);
  }
}

function normalizeEntries(data, filePath) {
  if (Array.isArray(data)) return data;
  if (Array.isArray(data.entries)) return data.entries;
  throw new Error(`${filePath}: expected JSON to be an array or an object with an entries array`);
}

function inferLanguage(filePath) {
  const match = path.basename(filePath).match(/^([a-z]{2})-translation-pack\.json$/u);
  if (!match) {
    throw new Error(`${filePath}: could not infer language code from filename`);
  }
  return match[1];
}

function placeholders(text) {
  return new Set(String(text ?? "").match(/\{[A-Za-z0-9_]+\}/gu) ?? []);
}

function sameSet(a, b) {
  if (a.size !== b.size) return false;
  for (const value of a) {
    if (!b.has(value)) return false;
  }
  return true;
}

function formatSet(set) {
  return [...set].sort().join(", ") || "(none)";
}

function defaultPackPaths() {
  return draftLanguages.map((language) => path.join("docs", "i18n", `${language}-translation-pack.json`));
}

function summarizeLanguage(language, dictionary) {
  return {
    language,
    keys: Object.keys(dictionary).length,
  };
}

function buildDictionary(filePath) {
  const language = inferLanguage(filePath);
  if (!draftLanguages.includes(language)) {
    throw new Error(`${filePath}: ${language} is not configured as a draft preview language`);
  }

  const entries = normalizeEntries(readJson(filePath), filePath);
  const dictionary = {};

  for (const [index, entry] of entries.entries()) {
    const row = index + 1;
    const key = entry.key;
    if (typeof key !== "string" || !key) {
      throw new Error(`${filePath}: row ${row} is missing a string key`);
    }
    if (Object.hasOwn(dictionary, key)) {
      throw new Error(`${filePath}: duplicate key: ${key}`);
    }

    const target = String(entry.target ?? "");
    if (!target.trim()) {
      throw new Error(`${filePath}: ${key}: target is blank`);
    }

    const sourcePlaceholders = placeholders(entry.en ?? entry.source ?? "");
    const targetPlaceholders = placeholders(target);
    if (!sameSet(sourcePlaceholders, targetPlaceholders)) {
      throw new Error(
        `${filePath}: ${key}: placeholder mismatch. source=${formatSet(sourcePlaceholders)} target=${formatSet(targetPlaceholders)}`
      );
    }

    dictionary[key] = target;
  }

  return { language, dictionary };
}

function writeJson(dictionaries) {
  fs.mkdirSync(path.dirname(outputJsonPath), { recursive: true });
  fs.writeFileSync(outputJsonPath, `${JSON.stringify(dictionaries, null, 2)}\n`);
}

function writeSource(dictionaries) {
  const source = [
    "// Generated by scripts/generate-draft-i18n-runtime-dictionaries.mjs. Do not edit by hand.",
    `export const draftRuntimeDictionaries = ${JSON.stringify(dictionaries, null, 2)};`,
    "",
    "export default draftRuntimeDictionaries;",
    "",
  ].join("\n");

  fs.mkdirSync(path.dirname(outputSourcePath), { recursive: true });
  fs.writeFileSync(outputSourcePath, source);
}

try {
  const inputPaths = packPaths.length ? packPaths : defaultPackPaths();
  if (inputPaths.some((arg) => arg === "--help" || arg === "-h")) {
    console.log(usage());
    process.exit(0);
  }

  const dictionaries = {};
  for (const filePath of inputPaths) {
    const { language, dictionary } = buildDictionary(filePath);
    if (Object.hasOwn(dictionaries, language)) {
      throw new Error(`${filePath}: duplicate language input: ${language}`);
    }
    dictionaries[language] = dictionary;
  }

  const missingLanguages = draftLanguages.filter((language) => !Object.hasOwn(dictionaries, language));
  if (missingLanguages.length) {
    throw new Error(`Missing draft language pack(s): ${missingLanguages.join(", ")}`);
  }

  const orderedDictionaries = Object.fromEntries(draftLanguages.map((language) => [language, dictionaries[language]]));
  writeJson(orderedDictionaries);
  writeSource(orderedDictionaries);

  console.table(draftLanguages.map((language) => summarizeLanguage(language, orderedDictionaries[language])));
  console.log(`\nWrote ${outputJsonPath}`);
  console.log(`Wrote ${outputSourcePath}`);
} catch (error) {
  console.error(error.message);
  process.exit(1);
}
